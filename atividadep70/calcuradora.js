
function somar(n1,n2){
    return n1+n2;
}

function subtrair(n1,n2){
    return n1-n2;
}

function dividir(n1,n2){
    return n1/n2;
}

function multiplicar(n1,n2){
    return n1*n2;
}

console.log("N1 é 10 e N2 é 5");
console.log("O resultado da soma é: ", somar(10,5));
console.log("O resultado da subtração é: ", subtrair(10,5));
console.log("O resultado da divisão é: ", dividir(10,5));
console.log("O resultado da multiplicação é: ", multiplicar(10,5));